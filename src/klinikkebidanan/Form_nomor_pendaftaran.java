        private void tampilkandata() {
        DefaultTableModel tabelmapel = new DefaultTableModel();
        tabelmapel.addColumn("Nomer Daftar");
        tabelmapel.addColumn("Tanggal");
        tabelmapel.addColumn("Jam");
        tabelmapel.addColumn("Nomer Rekam Medis");
        tabelmapel.addColumn("Nama");
        tabelmapel.addColumn("Periksa");
        try {
            open_db();
            String sql = "select * from daftar";
            stm = (Statement) Con.createStatement();
            ResultSet rs=stm.executeQuery(sql);
            while (rs.next()) {
                tabelmapel.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)});
            }
           jTable1.setModel(tabelmapel);
        } catch (Exception e) {
        }


    }
        
