        private void tampilkan_data() {
        DefaultTableModel tabelmapel = new DefaultTableModel();
        tabelmapel.addColumn("No. Transaksi");
        tabelmapel.addColumn("No. Periksa");
        tabelmapel.addColumn("Tanggal");
        tabelmapel.addColumn("Jam");
        tabelmapel.addColumn("Nama");
        tabelmapel.addColumn("Tindakan");
        tabelmapel.addColumn("Obat");
        tabelmapel.addColumn("Kasir");
        tabelmapel.addColumn("Total Bayar");
        tabelmapel.addColumn("Bayar");
        try {
            open_db();
            String sql = "select * from pembayaran";
            stm = (Statement) Con.createStatement();
            ResultSet rs=stm.executeQuery(sql);
            while (rs.next()) {
              tabelmapel.addRow(new Object[]{rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(6),rs.getString(7),rs.getString(8),rs.getString(9),rs.getString(10),rs.getString(11),rs.getString(12)});
            }
           jTable1.setModel(tabelmapel);
        } catch (Exception e) {
        }
    }
