    private void cmdsamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdsamaActionPerformed
        // TODO add your handling code here:
        switch(pilih)
{
case 1:
angka2 = Double.parseDouble(angka);
total = angka1 + angka2;
angka = Double.toString(total);
tampil.setText(angka);
break;
    case 2:
angka2 = Double.parseDouble(angka);
total = angka1 - angka2;
angka = Double.toString(total);
tampil.setText(angka);
break;
    case 3:
angka2 = Double.parseDouble(angka);
total = angka1 * angka2;
angka = Double.toString(total);
tampil.setText(angka);
break;
    case 4:
angka2 = Double.parseDouble(angka);
total = angka1 / angka2;
angka = Double.toString(total);
tampil.setText(angka);
break;
default:
break;
}

    }//GEN-LAST:event_cmdsamaActionPerformed

